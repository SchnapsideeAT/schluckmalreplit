/**
 * Get the SVG image path for a card based on its category and ID
 * @param category - The card category
 * @param cardId - The card ID from cards.json
 * @returns The imported SVG path
 */
export const getCardImage = (category: CardCategory, cardId: number): string => {
  const categoryImages = cardImages[category];
  
  if (!categoryImages || categoryImages.length === 0) {
    console.warn(`No images found for category: ${category}`);
    return "";
  }

  // Use modulo to cycle through available images if there are more cards than images
  const imageIndex = (cardId - 1) % categoryImages.length;
  return categoryImages[imageIndex];
};

/**
 * Get a random card image for a specific category
 * @param category - The card category
 * @returns A random SVG path from that category
 */
export const getRandomCardImage = (category: CardCategory): string => {
  const categoryImages = cardImages[category];
  
  if (!categoryImages || categoryImages.length === 0) {
    console.warn(`No images found for category: ${category}`);
    return "";
  }

  const randomIndex = Math.floor(Math.random() * categoryImages.length);
  return categoryImages[randomIndex];
};
